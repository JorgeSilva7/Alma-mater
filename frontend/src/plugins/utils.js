export function obtenerNombreRegion(nRegion) {
    switch (nRegion) {
        case 0:
            return "Metropolitana";
        case 1:
            return "de Tarapacá";
        case 2:
            return "de Antofagasta";
        case 3:
            return "de Atacama";
        case 4:
            return "de Coquimbo";
        case 5:
            return "de Valparaíso";
        case 6:
            return "del Libertador Gral. Bernardo O'Higgins";
        case 7:
            return "del Maule";
        case 8:
            return "del Bío Bío";
        case 9:
            return "del la Araucanía";
        case 10:
            return "de los Lagos";
        case 11:
            return "de Aysén del Gral. Ibáñez del Campo";
        case 12:
            return "de Magallanes y de la Antártica Chilena";
        case 13:
            return "---";
        case 14:
            return "de los Rios";
    }
}